// Game interface from API
interface GameProduct {
  id: string;
  slug: string;
  title: string;
  category: string;
  publisher: string;
  country: string;
  mainImage: string;
  coverImage?: string;
  rating: number;
  price: number;
  discountPercent?: number | undefined;
  platforms: string[];
  autoDelivery?: boolean;
}

function getCategoryFlagCode(name: string): string | null {
  return getCountryFlagCode(name);
}

function getCategoryIcon(category: string) {
  switch (category.toLowerCase()) {
    case "popular":
      return <Flame size={16} className="text-brutal-pink" />;
    case "fps":
      return <Zap size={16} className="text-brutal-blue" />;
    case "moba":
      return <TrendingUp size={16} className="text-brutal-green" />;
    case "rpg":
      return <TrendingUp size={16} className="text-brutal-yellow" />;
    case "adventure":
      return <Globe size={16} className="text-brutal-blue" />;
    default:
      return null;
  }
}

// Transform Product to GameProduct
function transformProductToGame(product: Product): GameProduct {
  // Use game details for publisher if available
  const publisher =
    product.gameDetails?.publisher || product.gameDetails?.developer;

  // Determine country from region OR category if category is a country
  const regionRaw = product.gameDetails?.region || "";
  let regionCode = regionRaw.toLowerCase();
  
  const categoryName = product.category?.name || "";
  const isCategoryCountry = ["thailand", "malaysia", "singapore", "indonesia", "philippines", "vietnam", "china", "united states", "global", "world"].includes(categoryName.toLowerCase());

  // If region is empty but category is a country, use category as region
  if (!regionCode && isCategoryCountry) {
     regionCode = categoryName.toLowerCase();
  }

  const countryMap: Record<string, string> = {
    th: "Thailand",
    my: "Malaysia",
    sg: "Singapore",
    id: "Indonesia",
    ph: "Philippines",
    vn: "Vietnam",
    cn: "China",
    us: "United States",
    global: "Global",
    world: "Global",
    // Map full names too
    thailand: "Thailand",
    malaysia: "Malaysia",
    singapore: "Singapore",
    indonesia: "Indonesia",
    philippines: "Philippines",
    vietnam: "Vietnam",
    china: "China",
    "united states": "United States",
  };

  let country = 
    countryMap[regionCode] || 
    countryMap[regionCode.replace(/\(.*\)/, "").trim()] || 
    (regionRaw ? regionRaw : "Global");

  // Get starting price from types (lowest displayPrice)
  const types = product.types || [];

  // Calculate starting price from displayPrice
  const validPrices = types
    .filter((t) => t.displayPrice && Number(t.displayPrice) > 0)
    .map((t) => Number(t.displayPrice));

  const startingPrice = validPrices.length > 0 ? Math.min(...validPrices) : 0;

  // Use the highest discountRate among active types (if any)
  const discountRates = types
    .map((t) =>
      typeof t.discountRate === "number" ? Number(t.discountRate) : undefined,
    )
    .filter((v): v is number => v !== undefined && !Number.isNaN(v));

  const discountPercent: number | undefined =
    discountRates.length > 0 ? Math.max(...discountRates) : undefined;

  return {
    id: product.id,
    slug: product.slug,
    title: product.name,
    category: categoryName || "Direct Top-Up",
    publisher: publisher || "Unknown Publisher",
    country: country,
    mainImage:
      product.imageUrl ||
      `https://placehold.co/400x400?text=${encodeURIComponent(product.name)}`,
    rating: product.averageRating || 4.5,
    price: startingPrice,
    discountPercent: discountPercent,
    platforms: product.gameDetails?.platforms || ["PC", "Mobile"],
    autoDelivery: product.gameDetails?.autoDelivery ?? true,
  };
}

